        private const string CACHE_KEY = "bist_all_symbols";

        public BistSymbolService(
            IHttpClientFactory _,         // DI uyumu için tutuldu
            IMemoryCache cache,
            ILogger<BistSymbolService> logger)
        {
            _cache  = cache;
            _logger = logger;
        }

        public Task<List<(string Symbol, string Name)>> GetAllSymbolsAsync()
        {
            if (_cache.TryGetValue(CACHE_KEY, out List<(string, string)>? cached) && cached != null)
                return Task.FromResult(cached);

            // Hardcoded 446 sembol — isimler Yahoo'dan ShortName ile gelecek
            var list = GetAllSymbols();

            _cache.Set(CACHE_KEY, list, new MemoryCacheEntryOptions
            {
                AbsoluteExpirationRelativeToNow = TimeSpan.FromHours(24),
                Size = list.Count
            });

            _logger.LogInformation("Hisse listesi yüklendi: {Count} sembol.", list.Count);
            return Task.FromResult(list);
        }

